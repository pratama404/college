public class hitung{

public static void main (String [] args) {
int a = 5;
int b = 3;
int sum = a + b; // Penambahan
int difference = a - b; // Pengurangan
int product = a * b; // Perkalian
int quotient = a / b; // Pembagian
int remainder = a % b; // Modulus
System.out.println("Penambahan: " + sum);
System.out.println("Pengurangan: " + difference);
System.out.println("Perkalian: " + product);
System.out.println("Pembagian: " + quotient);
System.out.println("Modulus: " + remainder);

}
}
