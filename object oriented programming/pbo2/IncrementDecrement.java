public class IncrementDecrement {
public static void main(String[] args) {
int count = 5;
// Operator increment
count++; // Increment count by 1
System.out.println("Count after increment: " + count); // 6
// Operator decrement
count--; // Decrement count by 1
System.out.println("Count after decrement: " + count); //  5

Integer number = new Integer(10);
// Operator increment
number++; // Increment number by 1
System.out.println("Number after increment: " + number); //  11
// Operator decrement
number--; // Decrement number by 1
System.out.println("Number after decrement: " + number); //10
}
}
